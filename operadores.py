# Operadores aritmeticos "+, **, -, /"

print(2 + 2)
print(3 * 4)
print(7 - 6)
print(16/4)
print(11 % 4)  # Modulo
print(11 // 4)  # Division de piso
print(2**3)

# Operadores en cadenas

print("Hola" + "Mundo")  # Concatenacion
print("Hola" * 3)  # Repeticion
print("Hola" + "Mundo" * 3)

# Operadores de comparacion <, >
print("Hola" == "hola")  # Igual que
print("Hola" != "hola")  # Diferente que
print(3 < 11)
print(3 > 11)

# Operadores de existencia
print("ola" in "Hola")  # Existencia
print("ola" not in "Hola")  # Inexistencia

# Operadores booleanos
print(True or False)  # or verifica que se cumpla una de las condiciones
print(True and False)  # and verifica que se cumplan las dos condiciones

# Operadores de asignacion
x = 1
x += 2
x *= 3
x %= 4
print(x)
